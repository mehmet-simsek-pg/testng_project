package konu_anlatimi;

public class Annotations {

    /**
     * Assert kullanimi selenium derslerinde kulladigimiz, junit4 kütüphanesini kullanmistik.
     * TestNG: New Generation
     * */

    /**
     * @BeforeSuit
     * @AfterSuit
     *
     * @BeforeClass -> Class dan önce calisir
     * @AfterClass -> Class dan sonra calisir
     *
     * @BeforeTest -> Her testten önce calisir
     * @AfterTest -> Her testten sonra calisir
     *
     * @BeforeMethod -> her methoddan önce calisir
     * @AfterMethod -> her methoddan sonra calisir
     *
     * @Test -> bir test methodu olarak metodu isaretler
     * */
}
